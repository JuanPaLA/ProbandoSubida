package jd1;

public class Car {
	String brand;
	String doors;		
	
	public Car(String brand, String doors) {
		super();
		this.brand = brand;
		this.doors = doors;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getDoors() {
		return doors;
	}
	public void setDoors(String doors) {
		this.doors = doors;
	}
	
	
}
